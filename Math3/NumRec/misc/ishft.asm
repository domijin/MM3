	ASSUME CS:CODE,DS:DGROUP,SS:DGROUP

	PUBLIC ISHFT
ISHFT	PROC FAR
	PUSH BP
	MOV BP,SP
	LES BX,DWORD PTR [BP+10]
	MOV AX,ES:[BX]
	MOV DX,ES:[BX]+2
	LES BX,DWORD PTR [BP+6]
	MOV CX,ES:[BX]

BEGIN:	SHL AX,1
	RCL DX,1
	LOOP BEGIN

	MOV SP,BP
	POP BP
	RET 08H
	ISHFT ENDP
CODE	ENDS
	END
